package com.pointhouse.chiguan.k1_3;

import com.alibaba.fastjson.annotation.JSONField;

import java.io.Serializable;
import java.util.List;

/**
 * 课程信息
 * Created by Maclaine on 2017/7/25.
 */

public class Lesson implements Serializable {
    private Integer id;
    private String abouts;
    private String content;
    private Integer courseId;
    private String title;
    private Integer hasExercises=0;//1(有无练习题 0:没有 1：有)
    private List<Media> audioList;
    private List<Media> videoList;
    private String thumb;//缩略图
    private Integer lessonType;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getAbouts() {
        return abouts;
    }

    public void setAbouts(String abouts) {
        this.abouts = abouts;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public Integer getCourseId() {
        return courseId;
    }

    public void setCourseId(Integer courseId) {
        this.courseId = courseId;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public Integer getHasExercises() {
        return hasExercises;
    }

    public void setHasExercises(Integer hasExercises) {
        this.hasExercises = hasExercises;
    }

    public List<Media> getAudioList() {
        return audioList;
    }

    public void setAudioList(List<Media> audioList) {
        this.audioList = audioList;
    }

    public List<Media> getVideoList() {
        return videoList;
    }

    public void setVideoList(List<Media> videoList) {
        this.videoList = videoList;
    }

    public String getThumb() {
        return thumb;
    }

    public void setThumb(String thumb) {
        this.thumb = thumb;
    }

    public Integer getLessonType() {
        return lessonType;
    }

    public void setLessonType(Integer lessonType) {
        this.lessonType = lessonType;
    }
}
